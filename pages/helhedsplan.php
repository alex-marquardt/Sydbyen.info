<h2>Resumé af helhedsplanen for sydbyen</h2><br/>
<p>Med Helhedsplanen for boligsocial indsats vil vi ’sammen’ bryde m&oslash;nstre i Haderslev Syd.<br/>
- Bryde m&oslash;nstre i bydelen ved at medvirke i samfundsliv og lokalt dagligliv.<br/>
- Bryde m&oslash;nstre ved at samarbejde om den boligsociale udvikling – det g&aelig;lder.</p> 

<p><strong>Hvad skal Helhedsplanen?</strong></p>

<p>Helhedsplanen skal styrke boligsociale indsatser og samarbejde med beboerne om at skabe udvikling for omr&aring;det og for beboere. 
I den hidtidige indsats har kun Hjortebroparkens beboere nydt godt af aktiviteter, der har givet &oslash;get trivsel og 
udviklet beboeres medborgerskab. En fremtidig samlet plan bygges op omkring f&aelig;lles m&aring;l og strategisk retning for hele bydelen. Frem til medio 2016 udvikles et robust samarbejde
mellem boligorganisationer, kommune og frivillige organisationer, der kan underst&oslash;tte trivsel og aktivitet. B&oslash;rn, unge og voksne skal gives muligheder for at bryde ud af ensomhed,
mistrivsel og lediggang med udgangspunkt i to indsatser: ’B&oslash;rn, unge og familier’ og ’Besk&aelig;ftigelse og uddannelse’.</p>

<p><strong>Hvorfor er det vigtigt?</strong></p>

<p>Tallene taler sit tydelige sprog i Haderslev Syd: Et stigende antal b&oslash;rn set i forhold til manglende fritidstilbud og aktivitetssteder, mange enlige fors&oslash;rgere, et stigende antal
unge og voksne uden for uddannelse og arbejdsmarked mv. 
Beboernes beretninger vidner desuden om f&oslash;lesen af opsplitning mellem kulturer og boligomr&aring;der. Ord som fordomme og mistillid er p&aring; tale.</p>

<p><strong>Hvordan gribes samarbejdet an?</strong></p>

<p>Med helhedsplanen griber vi ind i folks dagligliv i boligomr&aring;det – ikke kun for en kort bem&aelig;rkning, men for at skabe afs&aelig;t for udvikling, som bydelens beboere kan tage ejerskab til.
Derfor vil fagpersoner og professionelle have en ydmyg og nysgerrig tilgang, der viser respekt for borgernes egne oplevelser af ressourcer. 
Fagpersonernes rolle er ikke at l&oslash;se udfordringer for beboerne, men i samarbejde med beboerne - det bedste udgangspunkt for at beboerne tager ejerskab.</p>

<p><strong>Hvem samarbejder?</strong></p>

<p>’Centralen’ vil fortsat v&aelig;re det lokale omdrejningspunkt for drift og samarbejde mellem parterne Haderslev Syd. ’Centralen’ skal nu virke for hele bydelen. 
Projektleder er udpeget af b&aring;de HAB-bolig og Domea. Til ’Centralen’ knyttes lokale medarbejdere p&aring; fuld- og deltid. Dertil kommer en ny ’Fremskudt indsats’, som bemandes med
kommunens medarbejdere inden for omr&aring;derne B&oslash;rn- og Familie, Fritid og Kultur, Jobcenter og UU p&aring; deltid eller ad/hoc efter behov. 
Der etableres en styregruppe med ledende parter fra det to boligorganisationer og fra involverede forvaltninger i Haderslev Kommune. Endelig neds&aelig;ttes faglige referencegrupper,
der kan medvirke til faglig sparring og opf&oslash;lgning p&aring; indsatser. Her kan parter fra frivillige organisationer, erhvervsliv m.fl. inddrages.</p>

<p><strong>Hvordan f&oslash;lges op?</strong></p>

<p>Det er besluttet, at der skal ske halv&aring;rlige evalueringer over for Haderslev byr&aring;d. Det betyder, at der vil v&aelig;re et st&aelig;rkt fokus p&aring; opf&oslash;lgning p&aring; m&aring;l og succeskriterier.
Allerede fra projektstart vil opm&aelig;rksomheden v&aelig;re rettet mod en mulig etablering af et boligsocialt f&aelig;llessekretariat for Helhedsplan Haderslev Syd og Helhedsplan Varbergparken 
medio 2016. Et samarbejde om en f&aelig;lles fremtidig helhedsplan for Nord og Sydbyen skal senest igangs&aelig;ttes medio 2015. Det g&aelig;lder for s&aring;vel voksne som for b&oslash;rn og unge.
For s&aring;vel HAB-Bolig og Domea som for Haderslev Kommune.</p>

<h2>Hvem st&aring;r bag projektet?</h2><br/>

<p>Helhedsplan sydbyen er financieret af Landsbyggefondens midler, en kommunal andel samt boligforeningerne.
Forel&oslash;big skal projektet k&oslash;re til midsommer 2016, hvorefter det skal evalueres af beboere og beboerdemokrater, ansatte kommunalpolitikere og Landsbyggefonden.</p>
